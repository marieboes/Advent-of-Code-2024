def parse_grid(grid_str):
    return [list(line) for line in grid_str.strip().split('\n')]


def find_caret_position(grid):
    for i, row in enumerate(grid):
        for j, cell in enumerate(row):
            if cell in '^>v<':
                return i, j, cell
    raise ValueError("Caret not found in the grid.")


def rotate_caret(caret):
    directions = '^>v<'
    return directions[(directions.index(caret) + 1) % 4]


def move_and_detect_boundary(grid):
    i, j, caret = find_caret_position(grid)
    visited = set()
    direction_map = {'^': (-1, 0), '>': (0, 1), 'v': (1, 0), '<': (0, -1)}

    max_rows, max_cols = len(grid), len(grid[0])

    while True:
        visited.add((i, j, caret))

        di, dj = direction_map[caret]
        ni, nj = i + di, j + dj

        if 0 <= ni < max_rows and 0 <= nj < max_cols and grid[ni][nj] != '#':
            i, j = ni, nj
        else:
            caret = rotate_caret(caret)

        if (i, j, caret) in visited:
            return True  # Detected a loop

        if (i == 0 and caret == '^') or (j == max_cols - 1 and caret == '>') or \
                (i == max_rows - 1 and caret == 'v') or (j == 0 and caret == '<'):
            return False  # Reached a boundary


def modify_and_test_boundary(grid):
    count_loops = 0

    original_grid = [row[:] for row in grid]

    for i, row in enumerate(original_grid):
        for j, cell in enumerate(row):
            if cell == '.':
                grid[i][j] = '#'

                if move_and_detect_boundary(grid):
                    count_loops += 1
                    print("Loop detected in modified grid:")
                else:
                    print("Boundary reached, no loop detected.")

                # Restore the cell
                grid[i][j] = '.'

    print(f"Total number of grids where a loop was detected: {count_loops}")


